    private void aceptarBotonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_aceptarBotonMouseClicked
        Calendar date = datePicker.getCalendar();
         GregorianCalendar fecha;
        if(date != null){
           fecha = new GregorianCalendar(date.get(Calendar.YEAR), date.get(Calendar.MONTH), date.get(Calendar.DAY_OF_MONTH));
        }
        else{
            fecha = caballoActual.getFechaNac();
        }
        caballoActual.setFechaNac(fecha);
        
        int vic = Integer.parseInt(victoriasField.getText());
        caballoActual.setVictorias(vic);
        
        CaballoDB.modificarFechaVictorias(caballoActual.getId(),fecha, vic);
        
        victoriasField.setText(""+caballoActual.getVictorias());
        fechaField.setText(Herramienta.gregorianCalendarToString(caballoActual.getFechaNac()));
        
        JOptionPane.showMessageDialog(aceptarBoton, "Se han actualizado los datos");
    }//GEN-LAST:event_aceptarBotonMouseClicked


