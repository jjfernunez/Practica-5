    private void botonSiguienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonSiguienteMouseClicked
        
        if(CaballoDB.compruebaSiguiente()){
        Caballo caballo = CaballoDB.getCaballoSiguiente();
        caballoActual = caballo;
        razaField.setText(caballoActual.getRaza());
        alturaField.setText(""+caballoActual.getAltura());
        precioField.setText(""+caballoActual.getPrecio());
        victoriasField.setText(""+caballoActual.getVictorias());
        fechaField.setText(Herramienta.gregorianCalendarToString(caballoActual.getFechaNac()));
        botonAnterior.setEnabled(true);
        }
        else{
            if(!CaballoDB.compruebaSiguiente()){
                botonSiguiente.setEnabled(false);
                caballoActual = CaballoDB.devolverUltimo();
            }
        }
    }//GEN-LAST:event_botonSiguienteMouseClicked

