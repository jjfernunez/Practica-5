CREATE TABLE Jinete(
CODIGO NUMERIC(3) PRIMARY KEY,
NOMBRE VARCHAR(30),
SUELDO DECIMAL(8,2),
FECHA_NAC DATE,
FOTO VARCHAR(10),
VICTORIAS_TOTAL NUMERIC(5),
USUARIO VARCHAR(15) UNIQUE,
CONTRASENA VARCHAR(15)

);

CREATE TABLE Caballo(
ID NUMERIC(6) PRIMARY KEY,
jiCodigo NUMERIC(3),
RAZA VARCHAR(10),
PRECIO DECIMAL(10,2),
FECHA_NAC DATE,
ALTURA NUMERIC(3),
VICTORIAS NUMERIC(3),
CONSTRAINT FK_CA FOREIGN KEY(jiCodigo) REFERENCES Jinete(CODIGO)
);



